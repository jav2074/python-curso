#Son utilizados para definir una coleccion que no tiene indice

colors = {'Blue', 'Black', 'Red'}

print(type(colors)) #Muestra el tipo de dato
print(colors)

colors.add('White') #Metodo para agregar(al inicio) un dato a un set
print(colors)

colors.remove('Red')    #Metodo para eliminar un dato de un set
print(colors)

colors.clear()  #Metodo para limpiar los datos que contiene un set
print(colors)

